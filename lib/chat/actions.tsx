async function submitUserMessage(content: string, modelName:string) {
  'use server'

  const aiState = getMutableAIState<typeof AI>()

  aiState.update({
    ...aiState.get(),
    messages: [
      ...aiState.get().messages,
      {
        id: nanoid(),
        role: 'user',
        content
      }
    ]
  })

  let textStream: undefined | ReturnType<typeof createStreamableValue<string>>
  let textNode: undefined | React.ReactNode

  const result = await streamUI({
    model: ollama(modelName),
    initial: <SpinnerMessage />,    
    messages: [
      ...aiState.get().messages.map((message: any) => ({
        role: message.role,
        content: message.content,
        name: message.name
      }))
    ],
    text: ({ content, done, delta }) => {
      if (!textStream) {
        textStream = createStreamableValue('')
        textNode = <BotMessage content={textStream.value} />
      }

      if (done) {
        textStream.done()
        aiState.done({
          ...aiState.get(),
          messages: [
            ...aiState.get().messages,
            {
              id: nanoid(),
              role: 'assistant',
              content
            }
          ]
        })
      } else {
        textStream.update(delta)
      }

      return textNode
    },
    
  })

  return {
    id: nanoid(),
    display: result.value
  }
}

async function regenUserMessage(messageId: string) {
  'use server'

  const aiState = getMutableAIState<typeof AI>()
  const messages = [
    ...aiState.get().messages
];

// Find the index of the message with the given ID
let messageIndex = messages.findIndex(message => message.id === messageId);

// If such a message is found, truncate the array to only include messages before it
if (messageIndex !== -1) {
    messages.splice(messageIndex+1, messages.length - messageIndex);
} else {
    console.log("No message with id " + messageId + " was found.");
}
  aiState.update({
    ...aiState.get(),
    messages: messages
  })

  let textStream: undefined | ReturnType<typeof createStreamableValue<string>>
  let textNode: undefined | React.ReactNode

  const result = await streamUI({
    model: ollama('codestral:latest'),
    temperature: 0.8,
    initial: <SpinnerMessage />,    
    messages: [
      ...aiState.get().messages.map((message: any) => ({
        role: message.role,
        content: message.content,
        name: message.name
      }))
    ],
    text: ({ content, done, delta }) => {
      if (!textStream) {
        textStream = createStreamableValue('')
        textNode = <BotMessage content={textStream.value} />
      }

      if (done) {
        textStream.done()
        aiState.done({
          ...aiState.get(),
          messages: [
            ...aiState.get().messages,
            {
              id: nanoid(),
              role: 'assistant',
              content
            }
          ]
        })
      } else {
        textStream.update(delta)
      }

      return textNode
    },
    
  })

  return {
    id: nanoid(),
    display: result.value
  }
}


export type AIState = {
  chatId: string
  messages: Message[]
}

export type UIState = {
  id: string
  display: React.ReactNode
}[]

